    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        
        Radnik r=validiraj();
        if(r==null){
            return;
        }
        int id=Controller.getInstance().ubaciRadnika(r);
        
        if(id>0){
            JOptionPane.showMessageDialog(this,"Dodat je radnik sa id-em: "+id);
        }else{
            JOptionPane.showMessageDialog(this, "Greska pri unosu radnika");
            return;
            
        }
        
        int odg=JOptionPane.showConfirmDialog(this, "Da li zelite da nastavnite ubacivanje?", "Provera", JOptionPane.YES_NO_OPTION);
        if(odg==JOptionPane.YES_OPTION){
            pripremiFormu();
        }else{
            this.dispose();
        }
        
        
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

