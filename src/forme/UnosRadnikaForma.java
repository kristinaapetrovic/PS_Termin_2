    private void popuniComboBox() {
        
        List<OrganizacionaJedinica> lista=Controller.getInstance().vratiOJed();
        
        for(OrganizacionaJedinica o:lista){
            jComboBoxOJed.addItem(o);
        }
        lista.add(0,null);
    }

    private Radnik validiraj() {
        
        Radnik r=new Radnik();
        r.setId(0);
        if(jTextFieldRadnaKnjizica==null){
            return null;
        }
        String rk=jTextFieldRadnaKnjizica.getText();
        if(rk==null || rk.isEmpty() || rk.matches("[a-zA-Z]+") || rk.length()>10){
            JOptionPane.showMessageDialog(this, "Greska kod radne knjizice");
            return null;
        }
        
        r.setRadnaKnjizica(rk);
        if(jTextFieldIme==null){
            return null;
        }
        String ime=jTextFieldIme.getText();
        if(ime==null || ime.isEmpty() || !ime.matches("[a-zA-z]+") || ime.length()<2){
             JOptionPane.showMessageDialog(this, "Greska kod imena");
            return null;
        }
        r.setIme(ime);
        if(jTextFieldPrezime==null){
            return null;
        }
        String prezime=jTextFieldPrezime.getText();
        if(prezime==null || prezime.isEmpty() || !prezime.matches("[a-zA-z]+") || prezime.length()<2){
             JOptionPane.showMessageDialog(this, "Greska kod prezimena");
            return null;
        }
        r.setPrezime(prezime);
        if(jTextFieldJMBG==null){
            return null;
        }
        String jmbg=jTextFieldJMBG.getText();
        if(jmbg==null || jmbg.isEmpty() || jmbg.matches("[a-zA-z]+") || jmbg.length()!=13){
             JOptionPane.showMessageDialog(this, "Greska kod jmbg-a");
            return null;
        }
        r.setJmbg(jmbg);
        
        if(jComboBoxOJed.getSelectedItem()==null){
            return null;
        }
        OrganizacionaJedinica oj=(OrganizacionaJedinica) jComboBoxOJed.getSelectedItem();
        
        r.setOj(oj);
        
        return r;
    }

    private void pripremiFormu() {
        jTextFieldIme.setText("");
        jTextFieldJMBG.setText("");
        jTextFieldPrezime.setText("");
        jTextFieldRadnaKnjizica.setText("");
        jComboBoxOJed.setSelectedItem(null);
    }

    private void popuniFormu(Radnik r) {
        
        jTextFieldIme.setEnabled(false);
        jTextFieldJMBG.setEnabled(false);
        jTextFieldPrezime.setEnabled(false);
        jTextFieldRadnaKnjizica.setEnabled(false);
        jComboBoxOJed.setEnabled(false);
        
        
        jTextFieldIme.setText(r.getIme());
        jTextFieldJMBG.setText(r.getJmbg());
        jTextFieldPrezime.setText(r.getPrezime());
        jTextFieldRadnaKnjizica.setText(r.getRadnaKnjizica());
        jComboBoxOJed.setSelectedItem(r.getOj());
        
        
    }
