    private void jButtonSacuvajIzmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajIzmenuActionPerformed
        
        Radnik novi=validiraj();
       
        if(novi==null){
            return;
        }
         novi.setId(odabrani.getId()); 
        int odg=JOptionPane.showConfirmDialog(this, "Da li zelite da sacuvate promene?", "Provera", JOptionPane.YES_NO_OPTION);
        
        if(odg==JOptionPane.YES_OPTION){
            boolean uspesno=Controller.getInstance().izmeniRadnika(novi);
            
            if(uspesno){
                StringBuilder sb=new StringBuilder();
                if(!novi.getRadnaKnjizica().equals(odabrani.getRadnaKnjizica())){
                    sb.append("Stara rk: "+odabrani.getRadnaKnjizica()+", nova rk: "+novi.getRadnaKnjizica()+"\n");
                }
                if(!novi.getJmbg().equals(odabrani.getJmbg())){
                    sb.append("Stari jmbg: "+odabrani.getJmbg()+", novi jmbg: "+novi.getJmbg()+"\n");
                }
                if(!novi.getIme().equals(odabrani.getIme())){
                    sb.append("Staro ime: "+odabrani.getIme()+", novo ime: "+novi.getIme()+"\n");
                }
                if(!novi.getPrezime().equals(odabrani.getPrezime())){
                    sb.append("Staro prezime: "+odabrani.getPrezime()+", novo prezime: "+novi.getPrezime()+"\n");
                }
                if(!novi.getOj().getNaziv().equals(odabrani.getOj().getNaziv())){
                    sb.append("Stara jedinica: "+odabrani.getOj().getNaziv()+", nova jedinica: "+novi.getOj().getNaziv()+"\n");
                }
                String poruka;
                if(sb.isEmpty()) poruka="nista nije promenjeno";
                poruka=sb.toString();
                JOptionPane.showMessageDialog(this, poruka);
            }
            else{
                JOptionPane.showMessageDialog(this, "Greska pri izmeni radnika");
            }
            
            
        }
        
        
        
    }//GEN-LAST:event_jButtonSacuvajIzmenuActionPerformed

